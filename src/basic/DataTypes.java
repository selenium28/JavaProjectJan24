package basic;

public class DataTypes {

	public static void main(String[] args) {
//		Data Types
//		1. Primitive Data Type -> int, double, char & boolean
//		2. Non-Primitive Data Type --> String & Array
		
//		1. int 
		int m; // Declaration
		m = 5;  // value initialize
//		int -> datatype, a --> variable, 10 - value
		int a = 10; // Initialization
//		int a = 20;  // Duplicate variables not allowed in java
		System.out.println(a);
		int a1 = 30;
		System.out.println(a1);
		
//		2. Double
		double d = 33.33;
		System.out.println(d);  // type --> syso & CNRT + Space
		double d1 = 100.54;
		System.out.println(d1);
		
//		3. char
		char c = 'A';
		System.out.println(c);
		char c1 = 'A';
		
//		4. boolean
//		Boolean -> condition, true & false this are keywords in java as well as values.
		boolean b = true;
		boolean b1 = false;
		
		
		
//		5. String 
//		String is a sequence of char & String is a class in java.
		String s = "Prachi";
		String s1 = "Rajanikant";
		String s2 = "!@#$$%";
		
		System.out.println(s1);
		

	}

}
