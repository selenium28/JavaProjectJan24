package basic;

public class ForLoopConcept {

	public static void main(String[] args) {
//		int a = 1; --> Initialization
//		a <= 10; --> Condition
//		a++ --> Increment 
		
		for (int a = 1; a <= 10; a++) {
			System.out.println(a);
		}
		
		System.out.println("*******");
		
		for (int b = 10; b >= 1; b--) {
			System.out.println(b);
		}
	}

}
