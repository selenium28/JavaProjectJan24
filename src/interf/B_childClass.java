package interf;

public class B_childClass extends A_ParentClass{
//	It is inheriting the properties of parent class into child class is known as Inheritance.
//	When method name is same in parent class as well as in child class with same input parameters is known as method overriding.
	
	public void bike() {
		System.out.println("B_childClass Bike");
	}

	public void cycle() {
		System.out.println("B_childClass Cycle");
	}
	
	public static void thar() {
		System.out.println("B_childClass Thar");
	}

}
